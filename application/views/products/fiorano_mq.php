                    <p class="text-justify">
                        MQTT is a light weight event and message oriented protocol that allows devices/sensors to communicate with remote systems in an efficient and asynchronous fashion;
                        specially designed for resource constrained devices over unreliable networks with low-bandwidth and high-latency issues.
                    </p>
                    <p class="text-justify">
                        FioranoMQTT supports the MQTT 3.1.1 (now an OASIS Standard) protocol which runs simultaneously with the JMS Server (FioranoMQ).
                    </p>
                    <p class="text-justify">
                        MQTT uses the publish/subscribe message pattern enabling one-to-many message distribution and decoupling of applications.
                        The MQTT protocol embedded in the FioranoMQTT Server uses the same Topic destinations as FioranoMQ and automatically maps MQTT clients and JMS clients, alloying a Topic destination to concurrently host MQTT/JMS Publishers and Subscribers. Each MQTT packet is converted into a JMS BytesMessage for consumption by JMS subscribers, while a JMS message is converted into an MQTT packet for consumption by MQTT subscribers as required.
                    </p>
