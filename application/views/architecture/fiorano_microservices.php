                                <h3>Process-level, Coarse-grained Components</h3>
                                <div>
                                    <p>
                                        A Microservice is a unit of software that runs in its own process and is independently replaceable and upgradeable. Each such service can be thought of as a unix-pipe in the classical sense, receiving one or more inputs,
                                        applying business logic and producing one or more outputs. Fiorano Microservices are 'coarse-grained' but not so big and complex as to have hundreds of interfaces. Each service has a few inputs and a few outputs, though there are no actual limits imposed on these numbers.
                                        By default, each Fiorano Microservice runs in a separate process, though it is possible to group multiple services and run them in a single process.
                                    </p>
                                    <p>
                                        Application systems are composed of multiple services that interact with each other either via request/reply or via messaging. Since the underlying framework is standards-based JMS,
                                        which supports both request/reply and publish-subscribe, the implementation does not require any proprietary APIs. Importantly, if part of a system needs to be replaced/redeployed, then only the corresponding Service is replaced (or upgraded).
                                        There is no need to stop the overall application to replace a given service. This makes it possible to partition and redeploy an application at runtime, on an as-required basis.
                                    </p>
                                </div>
                            </div>
                            <!-- train section -->
                            <div class="bhoechie-tab-content">
                                <h3>Organized around Business Capabilities</h3>
                                <div>
                                   <p>
                                       Each Fiorano Microservice typically implements bounded business functionality. The Fiorano Microservice design approach attaches a User-interface (both for configuration and for interaction) with each Service. When a large system needs to be developed, the developers map the system to multiple
                                       components and then each component is implemented as a Fiorano Microservice, where each Service may (if required) also have a user-interface component associated with it.
                                   </p>

                                    <p>When a solution needs to be changed or modified, one only needs to modify and/or replace a particular Microservice (or set of Services) in the system, without affecting the rest of the running Services.</p>
                                </div>
                            </div>
