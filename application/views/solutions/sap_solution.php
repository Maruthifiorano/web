                    <ul class="nopad" >
                        <li>Company has hundreds of distributors across the country</li>
                        <li>Distributors send trucks to 45+ factories to pick up goods</li>
                        <li>Each Distributor has a credit limit; if the limit is exceeded, explicit approval is required from a Company manager to raise the credit limit</li>
                        <li>Situation complicated due to several issues</li>
                        <ul style="list-style-image:url(<?php echo base_url('assets/images/home')?>/bullet.gif);">
                            <li>Distributor vehicles arrive at Company production facilities at night</li>
                            <li>Approvals delayed till the next day cause significant disruption</li>
                            <li>Company managers need to manually log on to SAP from home to approve additional credit; bad internet connectivity prevents access to IT systems from remote locations; plus, the SAP interface used (R3) is poor and difficult to learn</li>
                        </ul>
                    </ul>
                </li>
                <li><strong class="b">The Solution: credit approval requests on mobile phones:</strong>
                    <ul class="nopad" >
                        <li>Company uses Fiorano flows to perform the complete "approval request" transaction over mobile phones</li>
                        <ul >
                            <li>Initial approval request processed by Fiorano and placed on a publically accessible portal</li>
                            <li>Fiorano generates an SMS that is sent to the GPRS-enabled mobile phone of the manager</li>
                            <li>Managers access the portal by clicking the link in the SMS and approve or reject the request in the portal</li>
                            <li>Fiorano updates SAP at the backend with the approved/rejected notice.</li>
                        </ul>
                    </ul>
                </li>
                <li><strong class="b">Benefits:</strong>
                    <ul class="nopad" >
                        <li>Managers no longer need to access SAP via cumbersome SAP client applications; all relevant information now available on their mobile phones</li>
                        <li>Lower learning curve; lower licensing costs (less SAP client licenses)</li>
                        <li>Significant cost and time-savings for distributors due to faster turnaround times for approval requests</li>
                    </ul>
                </li>
            </ul>
